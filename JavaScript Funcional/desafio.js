/* Construa funções para somar, subtrair, dividir
 e multiplicar, mas cada uma delas só pode ocupar 
uma única linha.*/
const soma = (a, b) => a + b;
const subtracao = (a, b) => a - b;
const multiplicacao = (a, b) => a * b;
const divisao = (a, b) => a / b;


const a = 10;
const b = 5;

const resultadoDaSoma = soma(a, b); // 15
const resultadoDaSubtracao = subtracao(a, b); // 5
const resultadoDaMultiplicacao = multiplicacao(a, b); // 50
const resultadoDaDivisao = divisao(a, b); // 2
